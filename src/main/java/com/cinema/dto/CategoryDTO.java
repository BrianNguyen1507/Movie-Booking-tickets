package com.cinema.dto;


public class CategoryDTO {
	
	private long id;

	private String name;

	public CategoryDTO() {
		super();
	}

	public CategoryDTO(long id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
